#!/usr/bin/env python3

from stack import Stack
from compf import Compf


class CompfK40(Compf):
    """
    К40: построение полностью скобочной формулы.

    Используем готовый алгоритм разбора из Compf,
    но вместо постфиксной записи строим выражение,
    в котором каждое действие заключено в скобки.
    """

    def __init__(self):
        # Вызываем конструктор родительского класса.
        super().__init__()

        # Стек для хранения подвыражений.
        self.r = Stack()

    def compile(self, text):
        """
        Компиляция выражения в полностью скобочную форму.
        """
        # Очищаем стек перед новым запуском.
        self.r = Stack()

        # Используем разбор из родительского класса.
        super().compile(text)

        # В стеке остаётся итоговое выражение.
        return self.r.top()

    def process_value(self, c):
        """
        Обработка операнда.
        Каждый операнд кладём в стек как готовое подвыражение.
        """
        self.r.push(c)

    def process_oper(self, c):
        """
        Обработка операции.
        Берём два последних подвыражения и собираем новое:
        (левый_операнд операция правый_операнд)
        """
        right = self.r.pop()
        left = self.r.pop()
        self.r.push(f"({left}{c}{right})")